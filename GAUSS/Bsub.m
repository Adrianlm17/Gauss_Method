function [x]=Bsub(A,b)
  [n,n]=size(A);
  x(n)=b(n)/A(n,n);
  for i=n-1:-1:1
    S=0;
    for j=i+1:n
      S=S+A(i,j)*x(j);
    endfor
    x(i)=(b(i)-S)/A(i,i);
  endfor
endfunction

